class TestForceExportAuditTrail:
    """Force-export must write an audit event to EventStore."""

    async def test_force_export_generates_audit_event(self):
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_test_stores(tmp)
            aid = await _create_project_and_artifact(stores)

            out_path = os.path.join(tmp, "artifact.md")
            result = await export_artifact(
                aid, out_path, stores, force=True, force_reason="Emergency debug export"
            )

            assert "error" not in result
            assert result.get("force_bypass") is True

            # Verify audit event was written
            events = await stores.event_store.query(
                artifact_id=aid,
                event_type="force_export",
            )
            assert len(events) >= 1
            audit_event = events[0]
            assert audit_event.event_type == "force_export"
            assert audit_event.metadata.get("bypassed_state") == "GENERATED"
            assert "Emergency debug export" in audit_event.metadata.get("reason", "")

    async def test_force_export_without_reason_records_default(self):
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_test_stores(tmp)
            aid = await _create_project_and_artifact(stores)

            out_path = os.path.join(tmp, "artifact.md")
            result = await export_artifact(aid, out_path, stores, force=True)

            assert "error" not in result

            # Audit event should exist with default reason
            events = await stores.event_store.query(
                artifact_id=aid,
                event_type="force_export",
            )
            assert len(events) >= 1
            assert "(no explicit reason provided)" in events[0].metadata.get("reason", "")

    async def test_force_export_rejected_records_audit(self):
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_test_stores(tmp)
            aid = await _create_project_and_artifact(stores)

            # Reject the artifact
            await review_reject(aid, stores, note="Bad quality")

            out_path = os.path.join(tmp, "artifact.md")
            result = await export_artifact(
                aid, out_path, stores, force=True, force_reason="Debug: inspect rejected artifact"
            )

            assert "error" not in result
            assert result.get("force_bypass_state") == "REJECTED"

            # Verify audit event
            events = await stores.event_store.query(
                artifact_id=aid,
                event_type="force_export",
            )
            assert len(events) >= 1
            assert events[0].metadata.get("bypassed_state") == "REJECTED"

    async def test_approved_export_no_audit_event(self):
        """Normal export of APPROVED artifact should NOT generate a force_export audit event."""
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_test_stores(tmp)
            aid = await _create_project_and_artifact(stores)

            # Approve the artifact
            await review_approve(aid, stores)

            out_path = os.path.join(tmp, "artifact.md")
            result = await export_artifact(aid, out_path, stores)

            assert "error" not in result
            assert result.get("force_bypass") is None  # No bypass

            # No force_export event should exist
            events = await stores.event_store.query(
                artifact_id=aid,
                event_type="force_export",
            )
            assert len(events) == 0


