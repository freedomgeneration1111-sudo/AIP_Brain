async def _create_test_stores(tmp_dir: str) -> ReviewExportStores:
    """Create ReviewExportStores with real persistent SQLite stores."""
    db_path = os.path.join(tmp_dir, "aip.db")

    artifact_store = VersionedArtifactStore(db_path)
    await artifact_store.initialize()

    event_store = QueryableEventStore(db_path)
    await event_store.initialize()

    project_store = SqliteProjectStore(db_path)
    await project_store.initialize()

    ecs_store = PersistentEcsStore(db_path, event_store=event_store)
    await ecs_store.initialize()

    return ReviewExportStores(
        artifact_store=artifact_store,
        ecs_store=ecs_store,
        event_store=event_store,
        project_store=project_store,
    )


async def _create_project_and_artifact(
    stores: ReviewExportStores,
    project_name: str = "test_project",
    artifact_id: str = "ask:abc123test",
    content: str = "This is a generated answer about AIP architecture.",
    source_ids: list[str] | None = None,
    source_types: list[str] | None = None,
    empty: bool = False,
) -> str:
    """Create a project and a GENERATED artifact (simulating ask --save-artifact).

    Returns the artifact_id.
    """
    # Create project
    await stores.project_store.create_project(project_name, project_name, domain=project_name)

    # Write artifact
    metadata = {
        "artifact_type": "ask_answer",
        "project_id": project_name,
        "project_name": project_name,
        "prompt": "What is the AIP architecture?",
        "model_slot": "synthesis",
        "model_name": "test-model",
        "source_ids": source_ids if source_ids is not None else ["chunk:conv1:0", "chunk:conv1:1"],
        "source_types": source_types if source_types is not None else ["conversation_chunk", "conversation_chunk"],
        "session_id": "ask:test-session",
        "generated_at": "2025-01-15T10:00:00+00:00",
    }

    artifact_content = "" if empty else content
    await stores.artifact_store.write(artifact_id, artifact_content, metadata)

    # ECS transition: None → GENERATED
    await stores.ecs_store.transition(
        artifact_id=artifact_id,
        from_state=None,
        to_state="GENERATED",
        actor="ask_pipeline",
        reason="Generated by ask pipeline — pending DEFINER review",
    )

    return artifact_id


