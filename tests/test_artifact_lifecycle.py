class TestExportAuditTrail:
    """Test 3: Every export — normal or force — has a traceable event."""

    async def test_normal_export_records_artifact_exported_event(self):
        """Normal export of APPROVED artifact records artifact_exported event."""
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_export_stores(tmp)
            await _create_project(stores, "test_project")

            # Create and approve an artifact
            aid = "ask:export_test"
            metadata = {
                "artifact_type": "ask_answer",
                "project_id": "test_project",
                "project_name": "test_project",
                "prompt": "Test export",
                "source_ids": ["chunk:conv1:0"],
                "source_types": ["conversation_chunk"],
                "generated_at": "2025-01-15T10:00:00+00:00",
            }
            await stores.artifact_store.write(aid, "Export test content", metadata)
            await stores.ecs_store.transition(aid, None, "GENERATED", "ask_pipeline", "Created")

            await review_approve(aid, stores)

            out_path = os.path.join(tmp, "export.md")
            result = await export_artifact(aid, out_path, stores, format="markdown")

            assert "error" not in result

            # Verify artifact_exported event was recorded
            events = await stores.event_store.query(
                artifact_id=aid,
                event_type="artifact_exported",
            )
            assert len(events) >= 1
            assert events[0].metadata.get("format") == "markdown"

    async def test_force_export_creates_force_event_not_artifact_exported(self):
        """Force-export creates a force_export event, not artifact_exported."""
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_export_stores(tmp)
            await _create_project(stores, "test_project")

            aid = "ask:force_test"
            metadata = {
                "artifact_type": "ask_answer",
                "project_id": "test_project",
                "project_name": "test_project",
                "source_ids": ["chunk:conv1:0"],
                "source_types": ["conversation_chunk"],
                "generated_at": "2025-01-15T10:00:00+00:00",
            }
            await stores.artifact_store.write(aid, "Force test content", metadata)
            await stores.ecs_store.transition(aid, None, "GENERATED", "ask_pipeline", "Created")

            out_path = os.path.join(tmp, "force_export.md")
            result = await export_artifact(aid, out_path, stores, force=True, force_reason="Debug")

            assert "error" not in result
            assert result.get("force_bypass") is True

            # Should have force_export event
            force_events = await stores.event_store.query(
                artifact_id=aid,
                event_type="force_export",
            )
            assert len(force_events) >= 1

            # Should NOT have artifact_exported event (force is different)
            normal_events = await stores.event_store.query(
                artifact_id=aid,
                event_type="artifact_exported",
            )
            assert len(normal_events) == 0

    async def test_force_export_never_looks_like_normal(self):
        """Force-export result includes force_bypass markers that normal export doesn't."""
        with tempfile.TemporaryDirectory() as tmp:
            stores = await _create_export_stores(tmp)
            await _create_project(stores, "test_project")

            # Create two artifacts: one approved, one not
            aid_normal = "ask:normal_export"
            aid_force = "ask:force_export"
            for aid in [aid_normal, aid_force]:
                metadata = {
                    "artifact_type": "ask_answer",
                    "project_id": "test_project",
                    "project_name": "test_project",
                    "source_ids": ["chunk:conv1:0"],
                    "source_types": ["conversation_chunk"],
                    "generated_at": "2025-01-15T10:00:00+00:00",
                }
                await stores.artifact_store.write(aid, f"Content for {aid}", metadata)
                await stores.ecs_store.transition(aid, None, "GENERATED", "ask_pipeline", "Created")

            await review_approve(aid_normal, stores)

            # Normal export
            normal_result = await export_artifact(aid_normal, os.path.join(tmp, "normal.md"), stores)
            # Force export
            force_result = await export_artifact(
                aid_force, os.path.join(tmp, "force.md"), stores, force=True, force_reason="Emergency"
            )

            # Normal export should NOT have force markers
            assert "force_bypass" not in normal_result
            assert "audit_recorded" not in normal_result

            # Force export MUST have force markers
            assert force_result["force_bypass"] is True
            assert force_result["force_bypass_state"] == "GENERATED"
            assert force_result["audit_recorded"] is True

            # Force-exported markdown must have SOVEREIGN OVERRIDE notice
            with open(os.path.join(tmp, "force.md")) as f:
                force_content = f.read()
            assert "SOVEREIGN OVERRIDE" in force_content
            assert "force_export: true" in force_content

            # Normal markdown must NOT have SOVEREIGN OVERRIDE notice
            with open(os.path.join(tmp, "normal.md")) as f:
                normal_content = f.read()
            assert "SOVEREIGN OVERRIDE" not in normal_content
            assert "force_export" not in normal_content


