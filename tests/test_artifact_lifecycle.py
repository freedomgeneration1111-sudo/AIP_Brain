async def _create_lifecycle_stores(tmp_dir: str) -> ArtifactLifecycleStores:
    """Create ArtifactLifecycleStores with real persistent SQLite stores."""
    db_path = os.path.join(tmp_dir, "aip.db")
    return await create_artifact_lifecycle_stores(db_path)


async def _create_export_stores(tmp_dir: str) -> ReviewExportStores:
    """Create ReviewExportStores with real persistent SQLite stores."""
    db_path = os.path.join(tmp_dir, "aip.db")
    return await create_review_export_stores(db_path)


async def _create_project(stores, project_name: str = "test_project"):
    """Create a project in the project store."""
    await stores.project_store.create_project(project_name, project_name, domain=project_name)


async def _seed_artifact(
    stores: ArtifactLifecycleStores,
    artifact_id: str = "ask:seed123",
    content: str = "Seeded artifact content about AIP architecture.",
    project_name: str = "test_project",
    source_ids: list[str] | None = None,
) -> str:
    """Seed a GENERATED artifact for testing."""
    await _create_project(stores, project_name)

    metadata = {
        "artifact_type": "ask_answer",
        "project_id": project_name,
        "project_name": project_name,
        "title": content[:80],
        "prompt": "What is the AIP architecture?",
        "model_slot": "synthesis",
        "model_name": "test-model",
        "source_ids": source_ids or ["chunk:conv1:0"],
        "source_types": ["conversation_chunk"],
        "session_id": "session:test",
        "generated_at": "2025-01-15T10:00:00+00:00",
    }

    await stores.artifact_store.write(artifact_id, content, metadata)
    await stores.ecs_store.transition(
        artifact_id=artifact_id,
        from_state=None,
        to_state="GENERATED",
        actor="ask_pipeline",
        reason="Generated by ask pipeline — pending DEFINER review",
    )

    return artifact_id


