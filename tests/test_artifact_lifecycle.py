# ---------------------------------------------------------------------------
# End-to-end lifecycle test
# ---------------------------------------------------------------------------


class TestFullLifecycle:
    """End-to-end test: GENERATED → REVIEWED → APPROVED → EXPORTED with full trace."""

    async def test_full_artifact_lifecycle(self):
        """Complete lifecycle: create → note → approve → export → ledger → dashboard."""
        with tempfile.TemporaryDirectory() as tmp:
            lifecycle_stores = await _create_lifecycle_stores(tmp)
            export_stores = await _create_export_stores(tmp)
            await _create_project(lifecycle_stores, "lifecycle_project")

            # 1. Create artifact
            create_result = await artifact_create(
                content=(
                    "This is a complete lifecycle test document. It demonstrates "
                    "the full GENERATED → REVIEWED → APPROVED → EXPORTED path."
                ),
                stores=lifecycle_stores,
                title="Lifecycle Test Document",
                description="Testing the full artifact lifecycle",
                tags=["test", "lifecycle"],
                project_name="lifecycle_project",
                source_ids=["chunk:conv1:0"],
                source_types=["conversation_chunk"],
            )
            assert "error" not in create_result
            aid = create_result["artifact_id"]
            assert create_result["lifecycle_state"] == "GENERATED"

            # 2. Add reviewer notes
            note_result = await review_add_note(aid, lifecycle_stores, note="Sources look solid. Minor style issues.")
            assert "error" not in note_result
            assert note_result["lifecycle_state"] == "GENERATED"  # Unchanged

            # 3. Approve (via export stores which have same DB)
            approve_result = await review_approve(aid, export_stores)
            assert "error" not in approve_result
            assert approve_result["lifecycle_state"] == "APPROVED"

            # 4. Export as markdown
            md_path = os.path.join(tmp, "lifecycle.md")
            export_result = await export_artifact(aid, md_path, export_stores, format="markdown")
            assert "error" not in export_result
            assert os.path.exists(md_path)

            # 5. Export as plain text
            txt_path = os.path.join(tmp, "lifecycle.txt")
            txt_result = await export_artifact(aid, txt_path, export_stores, format="text")
            assert "error" not in txt_result
            assert os.path.exists(txt_path)

            # 6. Invalidate ECS cache (export_stores modified DB behind lifecycle_stores' back)
            lifecycle_stores.ecs_store._cache_loaded = False
            await lifecycle_stores.ecs_store._load_state_from_db()

            # 7. Inspect ledger
            ledger_result = await artifact_ledger(aid, lifecycle_stores)
            assert "error" not in ledger_result
            assert ledger_result["current_state"] == "APPROVED"
            # Should have: artifact_created, ecs_transition (GENERATED), reviewer_note,
            # review_verdict (APPROVED), ecs_transition (REVIEWED), ecs_transition (APPROVED),
            # artifact_exported (markdown), artifact_exported (text)
            assert ledger_result["event_count"] >= 5

            # Verify specific ledger entries exist
            event_types = [e["event_type"] for e in ledger_result["ledger"]]
            assert "artifact_created" in event_types
            assert "reviewer_note" in event_types
            assert "artifact_exported" in event_types

            # 8. Dashboard should show the approved artifact
            dashboard_result = await review_dashboard(lifecycle_stores)
            assert dashboard_result["states"]["APPROVED"] >= 1
